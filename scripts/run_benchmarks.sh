#!/usr/bin/env bash
# scripts/run_benchmarks.sh
# Run all Criterion benchmarks and produce a summary report.
# Usage: ./scripts/run_benchmarks.sh [--save]

set -euo pipefail

SAVE=${1:-""}
PROJECT_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$PROJECT_ROOT"

echo "================================================"
echo "  barq-vault — Benchmark Suite"
echo "================================================"
echo ""
echo "  Machine: $(uname -m) / $(uname -s)"
echo "  Date:    $(date -u '+%Y-%m-%d %H:%M UTC')"
echo "  Rust:    $(rustc --version)"
echo ""

BENCH_CRATES=("barq-compress" "barq-index" "barq-store")

for crate in "${BENCH_CRATES[@]}"; do
    echo "-------- $crate --------"
    cargo bench -p "$crate" 2>&1 | grep -E "(time:|thrpt:|Warning)" | sed 's/^/  /'
    echo ""
done

if [[ "$SAVE" == "--save" ]]; then
    REPORT="docs/benchmark_report_$(date +%Y%m%d).md"
    echo "# Benchmark Report — $(date -u '+%Y-%m-%d')" > "$REPORT"
    echo "" >> "$REPORT"
    echo "Generated by \`./scripts/run_benchmarks.sh --save\`" >> "$REPORT"
    echo "" >> "$REPORT"
    for crate in "${BENCH_CRATES[@]}"; do
        echo "## $crate" >> "$REPORT"
        echo '```' >> "$REPORT"
        cargo bench -p "$crate" 2>&1 | grep "time:" >> "$REPORT"
        echo '```' >> "$REPORT"
        echo "" >> "$REPORT"
    done
    echo "Saved report to: $REPORT"
fi

echo "================================================"
echo "  BENCHMARKS COMPLETE"
echo "================================================"
